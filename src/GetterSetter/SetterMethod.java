package GetterSetter;

public class SetterMethod {

	public int no;
	public Double value;
	public float marks;
	public String name;

	public void setno(int no) {
		this.no = no;

	}

	public void setvalue(Double value) {
		this.value = value;

	}

	public void setmarks(float marks) {
		this.marks = marks;

	}

	public void setname(String name) {
		this.name = name;
	}

}
