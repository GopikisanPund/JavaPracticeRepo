package StringProgram;

public class StringImmutable {

    public static void main(String[] args) {
    
        // Declaring and initializing two strings
        String string1 = "Aakansha";
        String string2 = "Linge";
        
        // Concatenating string1 and string2 and storing the result in a new variable
        String concatenatedString = string1.concat(string2);
        
        // Printing the concatenated string
        System.out.println(concatenatedString);
    }
}
