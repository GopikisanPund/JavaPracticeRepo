package StringProgram;

public class StringTwoString {

	public static void main(String[] args) {
		
		String langnameString = "java";
		System.out.println("First String " + langnameString);
		String laString = "Python";
		System.out.println("Second String " + laString);
		
		System.out.println("String Concatination " + langnameString.concat(laString)); 
		
		
	}

}
